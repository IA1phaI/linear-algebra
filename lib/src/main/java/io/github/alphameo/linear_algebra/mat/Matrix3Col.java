package io.github.alphameo.linear_algebra.mat;

/**
 * Enum of matrix 3x3 columns for safe access.
 *
 * @since 1.0.0
 */
public enum Matrix3Col {
    /**
     * Column index 0
     *
     * @since 1.0.0
     */
    C0,
    /**
     * Column index 1
     *
     * @since 1.0.0
     */
    C1,
    /**
     * Column index 2
     *
     * @since 1.0.0
     */
    C2;
}
