package io.github.alphameo.linear_algebra.mat;

/**
 * Enum of matrix 4x4 rows for safe access.
 *
 * @since 1.0.0
 */
public enum Matrix4Row {
    /**
     * Row index 0
     *
     * @since 1.0.0
     */
    R0,
    /**
     * Row index 1
     *
     * @since 1.0.0
     */
    R1,
    /**
     * Row index 2
     *
     * @since 1.0.0
     */
    R2,
    /**
     * Row index 3
     *
     * @since 1.0.0
     */
    R3
}
