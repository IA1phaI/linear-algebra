package io.github.alphameo.linear_algebra.mat;

/**
 * Enum of matrix 3x3 rows for safe access.
 *
 * @since 1.0.0
 */
public enum Matrix3Row {
    /**
     * Row index 0
     *
     * @since 1.0.0
     */
    R0,
    /**
     * Row index 1
     *
     * @since 1.0.0
     */
    R1,
    /**
     * Row index 2
     *
     * @since 1.0.0
     */
    R2;
}
